public class Homework432 {
    public static void main(String [] args){
        int n = 5;
        int count = 0;
        int sum = 0;
        int [][]matric = Utils.stexcelEvTpelMatric(n, n);
        for(int i = 0; i < n; i++){
            for(int j = 0; j < n; j++){
                if((i+j)<=(n-1)){
                    if((i+j)%2 == 1){
                        count++;
                        sum = sum + matric[i][j];
                    }
                }
            }
        }
        System.out.print("mijin qarakusayin@ = " + Math.sqrt(sum/count));
    }
}
