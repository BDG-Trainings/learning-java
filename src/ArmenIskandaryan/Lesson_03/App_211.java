package ArmenIskandaryan.Lesson_03;

public class App_211 {
    public static void main(String[] args) {
        int [] arr = {5, 10, 2, -10, -20, 30, -25, 50, 10, -5};
        int sum = 0;
        int count = 0;
        for (int i = 0; i < arr.length; i++) {
            if (arr[i] > 0) {
                sum = sum + arr[i];
                count = count + 1;
            }
        }
        if (count > 0) {
            double mt = (double) sum / count;
            System.out.println("Միջին թվաբանականը հավասար է " + mt);
        } else {
            System.out.println("Միջին թվաբանականը հավասար է " + 0);
        }
    }
}
