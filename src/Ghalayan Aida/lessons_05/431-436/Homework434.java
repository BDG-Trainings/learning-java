package lessons_05;

import java.util.Random;

public class Homework434 {
    public static void main(String[]args){
        int n = 5;
        double[][]matric = new double[n][n];
        double sum = 0;
        Random rand = new Random();
        double a = 5.4;
        double b = 15.3;
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < n; j++) {
                matric[i][j] = 50*rand.nextDouble();
            }
        }
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < n; j++) {
                System.out.print(matric[i][j] + " , ");
            }
            System.out.println();
        }
        for(int i = 0; i < n; i++){
            for(int j = 0; j < n; j++){
                if((i+j)>=(n - 1)){
                    if(Math.abs(matric[i][j])> a && Math.abs(matric[i][j])<b){
                        sum = sum + matric[i][j];
                    }
                }
            }
        }
        System.out.print(sum);
    }
}
